#include "push_swap.h"

int	norminette(char *s)
{
	char	**split;
	int		j;

	split = ft_split(s, ' ');
	if (!split)
		return (ERROR);
	j = -1;
	while (split[++j])
	{
		if (check_str(split[j]) == ERROR)
		{
			free_split(split);
			return (ERROR);
		}
	}
	free_split(split);
	return (0);
}
