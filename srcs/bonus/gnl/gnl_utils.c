char	*ft_strdup(char *src)
{
	int		j;
	int		len;
	char	*str;

	len = 0;
	j = 0;
	while (src[len])
		len++;
	str = (char *)malloc(sizeof(*str) * (len + 1));
	if (!str)
		return (NULL);
	while (j < len)
	{
		str[j] = src[j];
		j++;
	}
	str[j] = '\0';
	return (str);
}

int	find_n(char *cache)
{
	if (!cache)
		return (0);
	while (*cache)
	{
		if (*cache == '\n')
			return (1);
		cache++;
	}
	return (0);
}

char	*ft_strchr(const char *s, int c)
{
	unsigned char	*pc;

	pc = (unsigned char *)s;
	while (*pc != '\0' && *pc != (unsigned char)c)
		pc++;
	if (*pc == (unsigned char)c)
		return ((char *)pc);
	else
		return (0);
}

